// Write a Rust program that performs basic Math operations - addition, subtraction, multiplication, and division operations on two integers.

fn main() {
    let num1 = 100;
    let num2 = 50;


    println!("{} + {} = {}", num1,num2,num1+num2);
    println!("{} - {} = {}", num1,num2,num1-num2);
    println!("{} x {} = {}", num1,num2,num1*num2);
    println!("{} / {} = {}", num1,num2,num1/num2);

}
